from random import randint
import logging
import os
import sys

class PackagePathFilter(logging.Filter):
    def filter(self, record):
        pathname = record.pathname
        record.relativepath = None
        abs_sys_paths = map(os.path.abspath, sys.path)
        for path in sorted(abs_sys_paths, key=len, reverse=True):  # longer paths first
            if not path.endswith(os.sep):
                path += os.sep
            if pathname.startswith(path):
                record.relativepath = os.path.relpath(pathname, path)
                break
        return True


def setup_logger(logger):
    log_format = f'%(levelname)-8s| %(relativepath)s:%(lineno)s: %(message)s'
    formatter = logging.Formatter(log_format)

    # StreamHandler allows streaming of logs in console
    streamHandler = logging.StreamHandler()
    streamHandler.setFormatter(formatter)
    streamHandler.addFilter(PackagePathFilter())
    streamHandler.setLevel(logging.INFO)  # Logs only error level and above messages to stream
    logger.addHandler(streamHandler)
    
    return logger


